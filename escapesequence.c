#include <stdio.h>
int main() {
    char c = 'a';
    c = 'c';
    c = '\n'; //represents a new line
    c = '\t'; //represents tab space
    printf("hello my name is \n karan");// \n represents different line
    printf("hello my name is \t karan");// \t represents tab space
    printf("hello my name is \" karan");// to print ' or ""
    printf("hello my name is \\ karan");// to print \ 
    // search in chat gpt for more 
    return 0;

}